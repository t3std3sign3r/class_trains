def display_hangman(tries):
    stages = [  # финальное состояние: голова, торс, обе руки, обе ноги
                '''
                    WASTED
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |     / \\
                   -
                ''',
                # голова, торс, обе руки, одна нога
                '''
                Осталась последняя попытка!
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |     / 
                   -
                ''',
                # голова, торс, обе руки
                '''
                Осталось 2 попытки
                   --------
                   |      |
                   |      O
                   |     \\|/
                   |      |
                   |      
                   -
                ''',
                # голова, торс и одна рука
                '''
                Осталось 3 попытки
                   --------
                   |      |
                   |      O
                   |     \\|
                   |      |
                   |     
                   -
                ''',
                # голова и торс
                '''
                Осталось 4 попытки
                   --------
                   |      |
                   |      O
                   |      |
                   |      |
                   |     
                   -
                ''',
                # голова
                '''
                Осталось 5 попыток
                   --------
                   |      |
                   |      O
                   |    
                   |      
                   |     
                   -
                ''',
                # начальное состояние
                '''
                Виселица уже приготовлена!
                   --------
                   |      |
                   |      
                   |    
                   |      
                   |     
                   -
                '''
    ]
    return stages[tries]


def get_word():
    return choice(word_list).upper()


def print_word(word, lst):
    for c in word:
        if c in lst:
            print(c, end=' ')
        else:
            print('_', end=' ')
    print()


def play(word):
    word_completion = '_ ' * len(word)  # строка, содержащая символы _ на каждую букву задуманного слова
    guessed = False  # сигнальная метка
    guessed_letters = []  # список уже названных букв
    guessed_words = []  # список уже названных слов
    tries = 6  # количество попыток
    print('Давайте играть в угадайку слов!')
    print(display_hangman(tries))
    print(word_completion)
    while True:
        char_input = input('Введите букву (или слово): ').upper()
        if not char_input.isalpha():
            print('Попробуйте ввести букву или слово русского алфавита!')
            continue
        if char_input in guessed_words or char_input in guessed_letters:
            print('Уже вводили')
            continue
        if len(char_input) > 1:
            if char_input == word:
                print('Поздравляем, вы угадали слово! Вы победили!')
                break
            else:
                guessed_words.append(char_input)
                tries -= 1
                print(f'Мое слово не {guessed_words[-1]}, подумай еще!')
                print(display_hangman(tries))
                print_word(word, guessed_letters)
                if tries == 0:
                    print(f'Вы не смогли угадать слово {word}')
                    break
                continue
        if char_input in word:
            guessed_letters.append(char_input)
            for c in word:
                if c not in guessed_letters:
                    print('Угадали букву')
                    print_word(word, guessed_letters)
                    guessed = False
                    break
                guessed = True
            if guessed:
                print_word(word, guessed_letters)
                print('Поздравляем, вы угадали слово! Вы победили!')
                break
        else:
            guessed_letters.append(char_input)
            tries -= 1
            print(display_hangman(tries))
            print_word(word, guessed_letters)
        if tries == 0:
            print(f'Вы не смогли угадать слово: {word}')
            break


still_play = True
while still_play:
    play(get_word())
    quest = input('Сыграем еще? ')
    if not quest.lower() in ['да', 'yes', 'ок', '+']:
        still_play = False
